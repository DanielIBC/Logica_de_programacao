var dia = "quarta";

switch (dia) {
    case "segunda":
        console.log("Dia de começar a semana");
        break;

    case "terça":
        console.log("Hora de aprender coisas novas");
        break;

    case "quarta":
        console.log("Metade da semana, continue firme");
        break;
    
    case "quinta":
        console.log("quase lá, mais um pouco de esforço");
        break;
    
    case "sexta":
        console.log("SEXTOOOU!");
        break;
    
    default: 
    console.log("Final de semana chegou");

}